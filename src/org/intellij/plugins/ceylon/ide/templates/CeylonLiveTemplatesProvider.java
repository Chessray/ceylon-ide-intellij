package org.intellij.plugins.ceylon.ide.templates;

import com.intellij.codeInsight.template.impl.DefaultLiveTemplatesProvider;
import org.jetbrains.annotations.Nullable;

public class CeylonLiveTemplatesProvider implements DefaultLiveTemplatesProvider {

    private static final String[] DEFAULT_TEMPLATES = new String[]{
            "/liveTemplates/CeylonLiveTemplates"
    };

    @Override
    public String[] getDefaultLiveTemplateFiles() {
        return DEFAULT_TEMPLATES;
    }

    @Nullable
    @Override
    public String[] getHiddenLiveTemplateFiles() {
        return null;
    }
}
